#include <iostream>
using namespace std;

int main ()
{
    float a, b, tong, hieu, tich, thuong;
    cout << "nhap a =";
    cin >> a;
    cout << "nhap b =";
    cin >> b;

    tong = a + b;
    hieu = a - b;
    tich = a * b;
    thuong = a / b;

    cout << "tong so =" << tong << endl;
    cout << "hieu so =" << hieu << endl;
    cout << "tich so =" << tich << endl;
    cout << "thuong so =" << thuong << endl;
}
    
